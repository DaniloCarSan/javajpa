    private void initListeners(){
        
        jFTFTelefone.getDocument().addDocumentListener(Validators.getDocumentListener(()->{
             
            this.isValidTelefone = true;
            jLTelefone.setText("");
            String msg;
            
            switch(jCBTipoNumero.getSelectedIndex())
            {
                case 0:
                    msg = Validators.validateTelefoneRequired(jFTFTelefone.getText());
                break;
                case 1:
                    msg = Validators.validateCelularRequired(jFTFTelefone.getText());
                break;
                default:
                    msg = Validators.validateNumberRequired(jFTFTelefone.getText());
            }
  
            if(msg != null)
            {
                jLTelefone.setText(msg);
                this.isValidTelefone = false;
            }
            
            this.isValidForm();
        }));
        
       
        
    }
    
    
