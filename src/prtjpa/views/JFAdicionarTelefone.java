    private void jBCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBCadastrarActionPerformed
        try
        {
            Telefone telefone = new Telefone();
            telefone.numero = jFTFTelefone.getText();
            telefone.tipo = jCBTipo.getSelectedIndex();
            telefone.representante_codigo = this.representante.codigo;
            
            if(TelefoneDao.getPerNumeroAndRepresentante(telefone.numero, telefone.representante_codigo) == null)
            {
                TelefoneDao.create(telefone);
                JOptionPane.showMessageDialog(this,"Numero cadastrado com sucesso !");
                this.dispose();
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Numero com j√° existe !");
            }
            
        } 
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
    }//GEN-LAST:event_jBCadastrarActionPerformed

