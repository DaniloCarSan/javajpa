    private void jBExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBExcluirActionPerformed
        
        try
        {
            Representante representante = this.getSelectedRepresentanteInTable();
        
            int showConfirmDialog = JOptionPane.showConfirmDialog(
                this,
                "Você realmente desejá excluir este representante ("+representante.nome+")",
                "Excluir resentante",
                JOptionPane.OK_CANCEL_OPTION
            );

            if(showConfirmDialog==0)
            {
                RepresentanteDao.delete(representante);
                JOptionPane.showConfirmDialog(
                    this,
                    "Representante excluido com sucesso !",
                    "INFO",
                    JOptionPane.CLOSED_OPTION
                );

                this.list();
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, "Ocorreu um erro ao excluir representantes !");
        }
    }//GEN-LAST:event_jBExcluirActionPerformed

