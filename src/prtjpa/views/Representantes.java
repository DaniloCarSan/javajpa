    public void list(){
        try
        {
            DefaultTableModel model = ( DefaultTableModel ) jTRepresentantes.getModel();
            jTRepresentantes.setRowSorter( new TableRowSorter(model));
            model.setRowCount(0);
        
            this.representantes = RepresentanteDao.list();
                 
            for (Representante representante :  this.representantes)
            {
                model.addRow( new Object[]{
                    representante.codigo,
                    representante.nome,
                    representante.cpf,
                    representante.empresa
                });
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, "Ocorreu um erro ao listar representantes !");
        }
    }

